class ArithmeticOperator
{
	public static void main(String[] args)
	{
		int a = 20;
		int b = 10;
		int c = a+b;
		int d = a-b;
		int e = a*b;
		int f = a/b;
		int g = a%b;
		System.out.println("Add :" + c);
		System.out.println("Sub :" + d);
		System.out.println("Mul :" + e);
		System.out.println("div :" + f);
		System.out.println("rem :" + g);
	}
}